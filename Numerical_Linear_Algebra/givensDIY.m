function [c,s] = givensDIY(x1,x2)
if x2 == 0
    c = 1; s = 0;
else
    if abs( x2 ) > abs(x1)
        t = x1 / x2; s = 1 / sqrt( 1 + t*t ); c = s * t;
    else
        t = x2 / x1; c = 1 / sqrt( 1 + t*t ); s = c * t;
    end
end
end
